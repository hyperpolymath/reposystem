use crate::a2ml::{self, A2mlDocument};
use crate::k9::K9Component;
use anyhow::{Context, Result};
use std::fmt::Write;
use std::fs;
use std::path::Path;

/// Generate a complete `contractile.just` file from all A2ML and K9 sources
/// found under `contractiles_dir`. Scans for `*.a2ml` and `*.k9.ncl` files.
pub fn emit_all(contractiles_dir: &Path) -> Result<String> {
    let mut output = String::new();
    writeln!(output, "# Auto-generated by: contractile gen-just").unwrap();
    writeln!(
        output,
        "# Source directory: {}",
        contractiles_dir.display()
    )
    .unwrap();
    writeln!(
        output,
        "# Re-generate with: contractile gen-just --dir {}",
        contractiles_dir.display()
    )
    .unwrap();
    writeln!(output, "#").unwrap();
    writeln!(output, "# SPDX-License-Identifier: PMPL-1.0-or-later").unwrap();
    writeln!(output).unwrap();

    // ── Scan for A2ML files ──
    let a2ml_files = find_files_with_extension(contractiles_dir, "a2ml")?;

    for path in &a2ml_files {
        let content = fs::read_to_string(path)
            .with_context(|| format!("reading A2ML file: {}", path.display()))?;
        let doc = a2ml::parse(&content)
            .with_context(|| format!("parsing A2ML file: {}", path.display()))?;

        let file_type = doc
            .file_type
            .as_deref()
            .unwrap_or("Unknown")
            .to_lowercase();

        match file_type.as_str() {
            "mustfile" => {
                writeln!(output, "{}", emit_must(&doc)).unwrap();
            }
            "trustfile" => {
                writeln!(output, "{}", emit_trust(&doc)).unwrap();
            }
            "dustfile" => {
                writeln!(output, "{}", emit_dust(&doc)).unwrap();
            }
            "intentfile" => {
                writeln!(output, "{}", emit_intend(&doc)).unwrap();
            }
            _ => {
                // Future contractile types: emit generic executable recipes.
                writeln!(output, "{}", emit_generic(&file_type, &doc)).unwrap();
            }
        }
    }

    // ── Scan for K9 Hunt-level files ──
    // K9 evaluation requires nickel, so we only emit stubs that call
    // `k9 run <file>` rather than inlining the commands. This keeps the
    // generated Justfile independent of nickel availability.
    let k9_files = find_files_with_extension(contractiles_dir, "ncl")?;
    if !k9_files.is_empty() {
        writeln!(output, "# === K9 Components ===").unwrap();
        writeln!(output).unwrap();
        for path in &k9_files {
            let filename = path
                .file_stem()
                .and_then(|s| s.to_str())
                .unwrap_or("unknown");
            // Strip the `.k9` portion if present (e.g., "setup-repo.k9" → "setup-repo").
            let base_name = filename.strip_suffix(".k9").unwrap_or(filename);
            let recipe_name = sanitise_recipe_name(&format!("k9-{}", base_name));

            writeln!(output, "# K9 component: {}", path.display()).unwrap();
            writeln!(output, "{}:", recipe_name).unwrap();
            writeln!(
                output,
                "    contractile k9 run {}",
                path.display()
            )
            .unwrap();
            writeln!(output).unwrap();
        }
    }

    Ok(output)
}

/// Emit Just recipes for a Mustfile A2ML document.
/// Creates a `must-check` aggregate recipe and individual `must-<name>` recipes
/// for each check that has a `run:` entry.
pub fn emit_must(doc: &A2mlDocument) -> String {
    let mut out = String::new();
    writeln!(out, "# === MUST (Physical State Checks) ===").unwrap();
    writeln!(out, "# Source: Mustfile.a2ml").unwrap();
    writeln!(out).unwrap();

    let items = doc.executable_items();
    if items.is_empty() {
        writeln!(out, "# (no executable checks found)").unwrap();
        return out;
    }

    // Collect recipe names for the aggregate target.
    let recipe_names: Vec<String> = items
        .iter()
        .map(|item| sanitise_recipe_name(&format!("must-{}", item.subsection)))
        .collect();

    // Aggregate recipe.
    writeln!(out, "# Run all must checks").unwrap();
    writeln!(out, "must-check: {}", recipe_names.join(" ")).unwrap();
    writeln!(out, "    @echo 'All must checks passed'").unwrap();
    writeln!(out).unwrap();

    // Individual recipes.
    for item in &items {
        let name = sanitise_recipe_name(&format!("must-{}", item.subsection));
        if let Some(desc) = item.description {
            writeln!(out, "# {}", desc).unwrap();
        }
        writeln!(out, "{}:", name).unwrap();
        writeln!(out, "    {}", item.command).unwrap();
        writeln!(out).unwrap();
    }

    out
}

/// Emit Just recipes for a Trustfile A2ML document.

/// Emit Just recipes for a Dustfile A2ML document.
/// Creates individual `dust-<name>` recipes for each recovery/rollback action.
/// No aggregate is generated because dust actions are typically invoked
/// selectively, not all at once.
pub fn emit_dust(doc: &A2mlDocument) -> String {
    let mut out = String::new();
    writeln!(out, "# === DUST (Recovery & Rollback) ===").unwrap();
    writeln!(out, "# Source: Dustfile.a2ml").unwrap();
    writeln!(out).unwrap();

    let items = doc.executable_items();
    if items.is_empty() {
        writeln!(out, "# (no executable recovery actions found)").unwrap();
        return out;
    }

    // List available dust actions.
    writeln!(out, "# List available dust recovery actions").unwrap();
    writeln!(out, "dust-status:").unwrap();
    for item in &items {
        let desc = item.description.unwrap_or(item.subsection);
        writeln!(
            out,
            "    @echo '  dust-{}: {} [{}]'",
            sanitise_recipe_name(item.subsection),
            desc,
            item.key
        )
        .unwrap();
    }
    writeln!(out).unwrap();

    for item in &items {
        let name = sanitise_recipe_name(&format!("dust-{}", item.subsection));
        if let Some(desc) = item.description {
            writeln!(out, "# {}", desc).unwrap();
        }
        writeln!(out, "{}:", name).unwrap();
        writeln!(out, "    @echo 'Executing {} for {}'", item.key, item.subsection).unwrap();
        writeln!(out, "    {}", item.command).unwrap();
        writeln!(out).unwrap();
    }

    out
}

/// Emit Just recipes for an Intentfile A2ML document.
/// Intentfiles are declarative — they have no executable commands. The emitted
/// recipe simply prints the declared intents as a readable checklist.
pub fn emit_intend(doc: &A2mlDocument) -> String {
    let mut out = String::new();
    writeln!(out, "# === INTEND (Declared Future Intent) ===").unwrap();
    writeln!(out, "# Source: Intentfile.a2ml").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "# Display declared future intents").unwrap();
    writeln!(out, "intend-list:").unwrap();
    writeln!(out, "    @echo '=== Declared Intent ==='").unwrap();

    for section in &doc.sections {
        writeln!(out, "    @echo ''").unwrap();
        writeln!(out, "    @echo '{}:'", section.name).unwrap();
        // Print direct entries (bullet points).
        for entry in &section.entries {
            writeln!(out, "    @echo '  - {}'", entry.value).unwrap();
        }
        // Print prose lines.
        for line in &section.prose {
            let trimmed = line.trim();
            if trimmed.starts_with('-') || trimmed.starts_with("- ") {
                writeln!(out, "    @echo '  {}'", trimmed).unwrap();
            }
        }
    }
    writeln!(out).unwrap();

    out
}

/// Emit Just recipes for K9 Hunt-level components that have already been
/// evaluated via `nickel export`. This is used when the caller has a
/// [`K9Component`] in hand (as opposed to the stub approach in `emit_all`).
pub fn emit_k9_evaluated(component: &K9Component) -> String {
    let mut out = String::new();

    let name = component
        .pedigree
        .as_ref()
        .and_then(|p| p.metadata.as_ref())
        .and_then(|m| m.name.as_deref())
        .unwrap_or("k9-component");

    writeln!(out, "# === K9: {} ===", name).unwrap();
    writeln!(out, "# Source: {}", component.source_path).unwrap();
    writeln!(out, "# Security level: {}", component.leash_level()).unwrap();
    writeln!(out).unwrap();

    for recipe in &component.recipes {
        let recipe_name = sanitise_recipe_name(&format!("k9-{}", recipe.name));

        if let Some(desc) = &recipe.description {
            writeln!(out, "# {}", desc).unwrap();
        }

        // Dependencies.
        let deps: Vec<String> = recipe
            .dependencies
            .iter()
            .map(|d| sanitise_recipe_name(&format!("k9-{}", d)))
            .collect();

        if deps.is_empty() {
            writeln!(out, "{}:", recipe_name).unwrap();
        } else {
            writeln!(out, "{}: {}", recipe_name, deps.join(" ")).unwrap();
        }

        for cmd in &recipe.commands {
            writeln!(out, "    {}", cmd).unwrap();
        }
        writeln!(out).unwrap();
    }

    out
}

/// Emit generic executable recipes for an unknown/future contractile type.
/// Uses the file type name as the recipe prefix.
pub fn emit_generic(type_name: &str, doc: &A2mlDocument) -> String {
    let mut out = String::new();
    let prefix = type_name.to_lowercase().replace("file", "");

    writeln!(out, "# === {} (auto-detected) ===", type_name.to_uppercase()).unwrap();
    writeln!(out).unwrap();

    let items = doc.executable_items();
    if items.is_empty() {
        writeln!(out, "# (no executable items found)").unwrap();
        return out;
    }

    for item in &items {
        let name = sanitise_recipe_name(&format!("{}-{}", prefix, item.subsection));
        if let Some(desc) = item.description {
            writeln!(out, "# {}", desc).unwrap();
        }
        writeln!(out, "{}:", name).unwrap();
        writeln!(out, "    {}", item.command).unwrap();
        writeln!(out).unwrap();
    }

    out
}

/// Sanitise a string for use as a Just recipe name.
/// Just recipe names allow alphanumerics, hyphens, and underscores.
fn sanitise_recipe_name(name: &str) -> String {
    name.chars()
        .map(|c| {
            if c.is_alphanumeric() || c == '-' || c == '_' {
                c
            } else {
                '-'
            }
        })
        .collect()
}

/// Recursively find all files with a given extension under a directory.
fn find_files_with_extension(dir: &Path, ext: &str) -> Result<Vec<std::path::PathBuf>> {
    let mut results = Vec::new();

    if !dir.is_dir() {
        return Ok(results);
    }

    for entry in fs::read_dir(dir).with_context(|| format!("reading directory: {}", dir.display()))? {
        let entry = entry?;
        let path = entry.path();

        if path.is_dir() {
            results.extend(find_files_with_extension(&path, ext)?);
        } else if path.extension().and_then(|e| e.to_str()) == Some(ext) {
            results.push(path);
        }
    }

    results.sort();
    Ok(results)
}
